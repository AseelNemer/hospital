                    <List component="nav">
                        <ListItemButton onClick={open1}>
                            <ListItemIcon>
                                <HomeIcon />
                            </ListItemIcon>
                            <ListItemText primary="Profile" />
                        </ListItemButton >
                        <ListItemButton onClick={open2}>
                            <ListItemIcon>
                                <HomeIcon />
                            </ListItemIcon>
                            <ListItemText primary="Schedule" />
                        </ListItemButton >
                    </List>
